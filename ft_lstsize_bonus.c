#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		len;

	len = 0;
	while (lst != NULL)
	{
		len++;
		lst = lst->next;
	}
	return (len);
}
/*
	1- Iterate the list as long as exist
	2- Each iteration increases the counter(len)
	3- Return len
*/